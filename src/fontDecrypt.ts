// Unicode 私有区域起始地址 (根据字体文件分析)
// 从 U+E3E8 开始，对应 gid 58344
const UNICODE_START = 0xE3E8;
const GID_START = 58344;

/**
 * 解密文本中的加密字符
 * @param encryptedText 包含加密字符的文本
 * @returns 解密后的文本
 */
export function decryptText(encryptedText: string): string {
  if (!encryptedText) return '';

  let result = '';
  
  for (const char of encryptedText) {
    const codePoint = char.codePointAt(0);
    
    // 检查是否在私有 Unicode 区域
    if (codePoint !== undefined && codePoint >= 0xE000 && codePoint <= 0xF8FF) {
      // 计算对应的 gid
      const gid = GID_START + (codePoint - UNICODE_START);
      
      // 查找真实字符
      const realChar = GID_TO_CHAR[gid];
      
      if (realChar) {
        result += realChar;
      } else {
        // 如果没有找到映射，保留原字符
        result += char;
      }
    } else {
      // 普通字符直接保留
      result += char;
    }
  }
  
  return result;
}

/**
 * 解密 HTML 内容中的加密字符
 * @param html 包含加密字符的 HTML
 * @returns 解密后的 HTML
 */
export function decryptHtml(html: string): string {
  if (!html) return '';

  // 解密文本节点
  let result = html;
  
  // 匹配 <p> 标签中的文本
  result = result.replace(/<p>(.*?)<\/p>/g, (match, p1) => {
    return '<p>' + decryptText(p1) + '</p>';
  });
  
  return result;
}

/**
 * 解密章节内容
 * @param content 章节内容（HTML 格式）
 * @returns 解密后的文本
 */
export function decryptChapterContent(content: string): string {
  if (!content) return '';
  
  // 首先处理 <p> 标签
  let text = content.replace(/<p>([^<]*)<\/p>/g, (match, p1) => {
    return decryptText(p1) + '\n';
  });
  
  // 处理 <br> 标签
  text = text.replace(/<br\s*\/?>/g, '\n');
  
  // 移除其他 HTML 标签
  text = text.replace(/<[^>]+>/g, '');
  
  // 解码 HTML 实体
  text = text.replace(/&nbsp;/g, ' ');
  text = text.replace(/&amp;/g, '&');
  text = text.replace(/&lt;/g, '<');
  text = text.replace(/&gt;/g, '>');
  text = text.replace(/&quot;/g, '"');
  
  return text.trim();
}